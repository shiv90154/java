public class variableInJava {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        int c = 15;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        a = 20;
        System.out.println(a);
        a = b;
        System.out.println(a);
    }

}