public class javaArrays {

    public static void main(String args[]) {
        int a[] = { 33, 3, 4, 5, };
        // System.out.println(a); we can not run java array in this way
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i] + " ");
        }
    }
}

